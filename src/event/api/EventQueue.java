package event.api;

import java.util.LinkedList;
import java.util.Queue;

public final class EventQueue
{
   private static final EventQueue SINGLETON = new EventQueue();

   private final Queue< Event > events_ = new LinkedList<Event>();

   ///////////////////////////////////////////////////////////////////////////////////////////////////////////

   private EventQueue()
   {
   }

   ///////////////////////////////////////////////////////////////////////////////////////////////////////////

   public static final EventQueue getInstance()
   {
      return SINGLETON;
   }

   ///////////////////////////////////////////////////////////////////////////////////////////////////////////

   public synchronized void addEvent( final Event event )
   {
      events_.offer( event );
   }

   ///////////////////////////////////////////////////////////////////////////////////////////////////////////

   public synchronized Event getEvent()
   {
      return events_.poll();
   }
}
